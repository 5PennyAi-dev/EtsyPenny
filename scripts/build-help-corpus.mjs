/**
 * Build the help chatbot's grounding corpus by concatenating every Markdown
 * article under src/content/docs/ into a single TypeScript string constant
 * that can be imported from both Vercel serverless functions and the local
 * Express dev server.
 *
 * Why not fs.readFile at runtime?
 *   - Vercel strips src/ from the serverless bundle by default.
 *   - Per-request file I/O on cold start is slower than a string import.
 *   - process.cwd() differs between local and Vercel — build-time bundling
 *     sidesteps path fragility.
 *
 * Wired into npm prebuild and predev:api so the file is always fresh.
 * The generated file is committed so fresh clones work without a manual step.
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { join, relative, sep, dirname } from 'node:path';
import matter from 'gray-matter';

const CONTENT_DIR = 'src/content/docs';
const OUT_FILE = 'lib/help/corpus-data.ts';

function walk(dir, acc = []) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, entry.name);
    if (entry.isDirectory()) walk(p, acc);
    else if (entry.name.endsWith('.md')) acc.push(p);
  }
  return acc;
}

function main() {
  if (!existsSync(CONTENT_DIR)) {
    console.error(`[build-help-corpus] ${CONTENT_DIR} not found`);
    process.exit(1);
  }

  const files = walk(CONTENT_DIR);
  const articles = files.map((file) => {
    const slug = relative(CONTENT_DIR, file).split(sep).join('/').replace(/\.md$/, '');
    const raw = readFileSync(file, 'utf8');
    const { data, content } = matter(raw);
    return {
      slug,
      title: data.title || slug,
      category: data.category || 'General',
      order: typeof data.order === 'number' ? data.order : 999,
      summary: data.summary || '',
      body: content.trim(),
    };
  });

  // Sort: category alpha, then order. "General" always first for consistent grounding.
  articles.sort((a, b) => {
    const catA = a.category === 'General' ? '\x00' : a.category;
    const catB = b.category === 'General' ? '\x00' : b.category;
    if (catA !== catB) return catA.localeCompare(catB);
    return a.order - b.order;
  });

  const corpus = articles
    .map((a) => {
      const header = `# ${a.category} — ${a.title}`;
      const summary = a.summary ? `\n\n${a.summary}` : '';
      return `${header}${summary}\n\n${a.body}`;
    })
    .join('\n\n---\n\n');

  const meta = {
    articleCount: articles.length,
    totalChars: corpus.length,
    builtAt: new Date().toISOString(),
  };

  // TS template-literal-safe escaping
  const escaped = corpus
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');

  const outContent = `// AUTO-GENERATED by scripts/build-help-corpus.mjs — DO NOT EDIT BY HAND.
// Rebuild with: node scripts/build-help-corpus.mjs
// Source files: src/content/docs/**/*.md

export const HELP_CORPUS: string = \`${escaped}\`;

export const HELP_CORPUS_META = ${JSON.stringify(meta, null, 2)} as const;
`;

  const outDir = dirname(OUT_FILE);
  if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });
  writeFileSync(OUT_FILE, outContent, 'utf8');

  console.log(`[build-help-corpus] wrote ${OUT_FILE}`);
  console.log(`  articles: ${meta.articleCount}`);
  console.log(`  chars:    ${meta.totalChars.toLocaleString()}`);
  console.log(`  built:    ${meta.builtAt}`);
}

main();
